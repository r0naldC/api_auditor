            var url1 = new List<string>
            {
                "http://localhost:50618/reporte/VisorListadoProducto.aspx?b=&p=t&al=0&tipo=7"
            };

            using var handler = new HttpClientHandler
            {
                AllowAutoRedirect = false,
                UseCookies = false
            };

            using var client = new HttpClient(handler)
            {
                Timeout = TimeSpan.FromSeconds(15)
            };

            var results = new List<string>();
            results.Add("Url,StatusCode,AccessibleWithoutToken,Notes");
            var results200 = new List<string>();
            results200.Add("Url,StatusCode,AccessibleWithoutToken,Notes");
            foreach (var url in url1)
            {
                try
                {
                    using var request = new HttpRequestMessage(HttpMethod.Get, url);
                    request.Headers.TryAddWithoutValidation("User-Agent", "Mozilla/5.0");

                    using var response = await client.SendAsync(request);

                    bool accessible = response.StatusCode == HttpStatusCode.OK ||
                                      response.StatusCode == HttpStatusCode.Accepted ||
                                      response.StatusCode == HttpStatusCode.NoContent;

                    string notes =
                        response.StatusCode == HttpStatusCode.Unauthorized ? "401 Unauthorized" :
                        response.StatusCode == HttpStatusCode.Forbidden ? "403 Forbidden" :
                        response.StatusCode == HttpStatusCode.Redirect ||
                        response.StatusCode == HttpStatusCode.MovedPermanently ||
                        response.StatusCode == HttpStatusCode.Found ? "Redirect (possible login)" :
                        "";

                    results.Add($"{Escape(url)},{(int)response.StatusCode},{accessible},{Escape(notes)}");
                    
                    if ((int)response.StatusCode == 200)
                    {
                        Console.ForegroundColor = ConsoleColor.DarkRed;
                        results200.Add($"{Escape(url)},{(int)response.StatusCode},{accessible},{Escape(notes)}");
                    }
                    if((int)response.StatusCode == 302) Console.ForegroundColor = ConsoleColor.DarkCyan;
                    if((int)response.StatusCode == 404) Console.ForegroundColor = ConsoleColor.DarkYellow;

                    Console.WriteLine($"{url} -> {(int)response.StatusCode} {response.ReasonPhrase}");
                    Console.ResetColor();
                }
                catch (TaskCanceledException)
                {
                    results.Add($"{Escape(url)},TIMEOUT,false,Timeout");
                    Console.WriteLine($"{url} -> TIMEOUT");
                }
                catch (Exception ex)
                {
                    results.Add($"{Escape(url)},ERROR,false,{Escape(ex.Message)}");
                    Console.WriteLine($"{url} -> ERROR: {ex.Message}");
                }
            }

            File.WriteAllLines("url_visores_check_results_new.csv", results);
            Console.WriteLine("CSV generado: url_check_results_new.csv");
            File.WriteAllLines("url_visores_check_results_200_new.csv", results200);
            Console.WriteLine("CSV generado: url_check_results_200_new.csv");
        }

        static string Escape(string value)
            => "\"" + value.Replace("\"", "\"\"") + "\"";
